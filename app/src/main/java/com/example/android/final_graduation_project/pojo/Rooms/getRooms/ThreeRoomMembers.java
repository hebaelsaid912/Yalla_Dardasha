package com.example.android.final_graduation_project.pojo.Rooms.getRooms;

public class ThreeRoomMembers {
    private boolean is_moderator;
    private boolean is_speaker;
    private String user_name;
    private String user_id;
    private String user_image;

    public boolean isIs_moderator() {
        return is_moderator;
    }

    public void setIs_moderator(boolean is_moderator) {
        this.is_moderator = is_moderator;
    }

    public boolean isIs_speaker() {
        return is_speaker;
    }

    public void setIs_speaker(boolean is_speaker) {
        this.is_speaker = is_speaker;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getUser_image() {
        return user_image;
    }

    public void setUser_image(String user_image) {
        this.user_image = user_image;
    }
}
